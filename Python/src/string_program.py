#In Python, the string data type can be represented using single quotes, double quotes, and triple quotes.

#Single quotes are used to represent a string. For example:

myStr = 'Python is a famous programming language'

print(type(myStr))

#Double quotes are used to represent a string. For example:

myStr = "Python is a famous programming language"

print(type(myStr))

#Triple quotes are used to represent a string.Triple quotes string is mainly used for multiple line strings 
#For example 1:

myStr = """Python 
        is a famous
        programming 
        language"""

print(type(myStr))

myStr = '''Python 
        is a famous
        programming 
        language'''

print(type(myStr))